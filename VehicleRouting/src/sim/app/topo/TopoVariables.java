package sim.app.topo;

public class TopoVariables {

	public final int vehicles;
	public final int destinations;
	public final int height;
	public final int width;
	
	public TopoVariables(int vehicles, int destinations, int height, int width) {
		this.vehicles = vehicles;
		this.destinations = destinations;
		this.height = height;
		this.width = width;
	}
}
